<?php

class ChartsController extends AppController {

//-----------------------------------------------------------------------------------------------------------------------------------------------------//
//--------------------------------------------------- DEFAULT FUNCTION TO GRAB AND DISPLAY DATA -------------------------------------------------------//
//-----------------------------------------------------------------------------------------------------------------------------------------------------//
    public function index() {																														//	\
        $this->layout = 'charts';																													//	\
		$PlotID=str_replace('http://www.cadwolf.com/Charts/','',Router::url(null, true));															//	\
		Controller::loadModel('Plot');																												//	\
		$this->set('PlotData', $this->Plot->find('first', array('conditions' => array('id' => $PlotID)));											//	\
    }																																				//	\
//-----------------------------------------------------------------------------------------------------------------------------------------------------//

}


?>